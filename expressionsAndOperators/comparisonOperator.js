// Equal to 
let equalTo = "3" == 3; // (true) == checks only for the value
console.log(equalTo);

//Strict Equals To

let strictEqualTo = "3" === 3; // (false) === checks for data type as well as the value
console.log(strictEqualTo)

//Not Equal To
let notEqualTo = 3 != "3";
console.log(notEqualTo);

//Strict Not Equal To
let strictNotEqualTo = 3 !== "3";
console.log(strictNotEqualTo);

//Greater Than
let greaterThan = 4 > 3; //(true) because 4 is greater than 3.
console.log(greaterThan);

// Less than
let lessThan = 33 < 100; //(true) becuase 33 is less than 100. 
console.log(lessThan);

//Greater Than Or Equal
let greaterThanOrEqual = 3 >= 3;
console.log(greaterThanOrEqual);

//Less Than Or Equal To
let lessThanOrEqualTo = 2 <= 3;
console.log(lessThanOrEqualTo);//(true) becuase 2 is indeed less than or equal to 3.

